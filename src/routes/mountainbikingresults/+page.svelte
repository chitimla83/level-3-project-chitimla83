  <h2>SISS Cycling</h2>
  <p>
    Over the weekend of 9-10 July, Olivia Ewing, Zoe Spillane and Isabella Inglis competed in the NZ South Island Cycling Champs at Ruapuna Raceway in Christchurch. <br /> On the first day the girls rode in the rainy weather with Olivia placing 5th in U15, Zoe 2nd in U17 and Isabella 9th in U20. On the second day the wind came up, but fortunately there was no rain. Olivia sprinted to 3rd in the U15 race and Zoe rode in a 3 person breakaway in the U17 race, sprinting to 3rd. Overall Craighead placed 12th equal in the total points out of 41 schools.
    <br /> The weekend wouldn’t have been possible without our team manager, Ryan Spillane.
    <br />
    Zoe and Olivia also competed in Blenheim in the Skoda Southern Tour , which is part of the Cycling NZ Road and Track national series.
    <br />
    Both girls were at the top of their bunches and competed very well. The tour consisted of four different individual races over the 2 days, including a time trial, hill climb, road race and also national titles were up for the taking in the Criterium street race.
    <br />
    Both Zoe and Olivia placed on the podium in all 4 races in their separate grades, Olivia added to this by winning the Criterium and with this taking home a championship jersey , National title and Gold Medal !!
    <br />
    Overall for the weekend both girls finished the tour in 2nd position in their grades, which is an excellent result in such tough competition!!
